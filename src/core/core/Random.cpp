#include "core/core/Random.h"

#include <stdio.h>
#include <time.h>

////////////////////////////////////////////////////////////////////////////////

crRandomGenerator::crRandomGenerator()
{
    mSeed = time(0);
    srand(mSeed);
}

////////////////////////////////////////////////////////////////////////////////

crRandomGenerator::~crRandomGenerator()
{
}

////////////////////////////////////////////////////////////////////////////////

double crRandomGenerator::getRandomNumber()
{
    return (static_cast<double>(rand_r(&mSeed) % 1000) / 10);
}

////////////////////////////////////////////////////////////////////////////////

int crRandomGenerator::getRandomInteger(int limit)
{
    // checkAndReturn(limit == 0, 0);
    return (rand_r(&mSeed) % limit);
}

////////////////////////////////////////////////////////////////////////////////

void crRandomGenerator::setSeed(unsigned int seedVal)
{
    mSeed = seedVal;
    srand(mSeed);
}

////////////////////////////////////////////////////////////////////////////////

const unsigned int& crRandomGenerator::getSeed() const 
{
    return mSeed;
}

